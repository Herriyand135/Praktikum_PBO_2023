package praktikum2;
import java.util.Scanner;

public class Ifelse {
	public static void main(String args[]) {
		Scanner input = new Scanner(System.in);
		int angka;
		
		System.out.print("Masukan Angka = ");
		angka = input.nextInt();
		
		//Pengkondisian
		if (angka > 5) {
			System.out.print("Angka yang anada masukan lebih dari 5 ");
		}else {
			System.out.print("Angka yang anada masukan lebih dari 5 ");
		}
		
	}
}
